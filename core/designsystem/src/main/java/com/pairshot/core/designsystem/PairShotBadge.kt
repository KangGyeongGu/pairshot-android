package com.pairshot.core.designsystem

import androidx.compose.ui.unit.dp

/** Badge / overlay indicator dimensions for cards. */
object PairShotBadge {
    /** Outer badge size (e.g. combined-pair indicator on PairCard). */
    val size = 28.dp

    /** Inner icon size within the badge. */
    val iconSize = 20.dp

    /** Padding between badge border and inner icon. */
    val iconPadding = 4.dp

    /** Distance from the card edge to the badge. */
    val edgeInset = 8.dp
}
