package com.pairshot.core.designsystem

import androidx.compose.ui.unit.dp

/** Standard card layout dimensions (SettingsCard etc.). */
object PairShotCard {
    /** Padding applied to the inner rows of a card (left/right and item vertical). */
    val innerPadding = 16.dp

    /** Padding between sibling cards or between a card and the next section. */
    val gap = 16.dp

    /** Corner radius used by Material's medium shape — reflected here for consistency. */
    val corner = 16.dp
}
